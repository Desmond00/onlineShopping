package p1;

public class Add
{
    int fno,sno;
    public void setFno(int fno)
    {
        this.fno=fno;
    }
    public int getFno()
    {
        return fno;
    }
    public void setSno(int sno)
    {
        this.sno=sno;
    }
    public int getSno()
    {
        return sno;
    }
    public int Addition()
    {
        return fno+sno;
    }
}